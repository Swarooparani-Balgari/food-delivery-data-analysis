SELECT * FROM ORDERS;

SELECT 
  SUM((QUANTITY*PRICE)+DELIVERYFEE) AS TOTALREVENUE
FROM ORDERS;

SELECT
  CITY,
  SUM((QUANTITY*PRICE)+DELIVERYFEE) AS TOTALREVENUE
FROM ORDERS
GROUP BY CITY
ORDER BY TOTALREVENUE DESC;

SELECT 
  CATEGORY,
  SUM((QUANTITY*PRICE)+DELIVERYFEE) AS TOTALREVENUE
FROM ORDERS
GROUP BY CATEGORY
ORDER BY TOTALREVENUE DESC;

SELECT TOP 1
  CUSTOMER,
  SUM((QUANTITY*PRICE)+DELIVERYFEE) AS TOTALREVENUE
FROM ORDERS
GROUP BY CUSTOMER
ORDER BY TOTALREVENUE DESC;

SELECT 
  ORDERID,
  (QUANTITY*PRICE)+DELIVERYFEE AS TOTALREVENUE
FROM ORDERS
